# day02 学习四种Python预设数据类型
print(100)
print(1.25)
print("string")
print(True)

# type()函数可以返回变量的类型
a = 99
b = 4.01
c = "String"
d = True
print(type(a))
print(type(b))
print(type(c))
print(type(d))

# 变量之间的运算
print(a + b)
print(a / b)
print(a // b)

# 变量类型转换
print(float(a))
print(int(b))
# 只要参数不为空，bool()返回就为true
e = "False"
print(bool(e))